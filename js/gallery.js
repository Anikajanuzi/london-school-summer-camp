const lightbox = document.getElementById("galleryLightbox");
const lightboxImage = document.getElementById("lightboxImage");
const lightboxCaption = document.getElementById("lightboxCaption");
const closeButton = document.querySelector(".lightbox-close");
const previousButton = document.querySelector(".lightbox-prev");
const nextButton = document.querySelector(".lightbox-next");

let currentGallery = [];
let currentPhoto = 0;

function showPhoto(index) {
  currentPhoto = index;

  if (currentPhoto < 0) {
    currentPhoto = currentGallery.length - 1;
  }

  if (currentPhoto >= currentGallery.length) {
    currentPhoto = 0;
  }

  lightboxImage.src = currentGallery[currentPhoto];
}

function openGallery(year) {
  currentGallery = galleries[year];
  currentPhoto = 0;

  lightboxImage.src = currentGallery[0];
  lightboxCaption.textContent = year;

  lightbox.classList.add("is-open");
}

closeButton.addEventListener("click", () => {
  lightbox.classList.remove("is-open");
});

nextButton.addEventListener("click", () => {
  showPhoto(currentPhoto + 1);
});

previousButton.addEventListener("click", () => {
  showPhoto(currentPhoto - 1);
});

document.addEventListener("keydown", (e) => {
  if (!lightbox.classList.contains("is-open")) return;

  if (e.key === "ArrowRight") {
    showPhoto(currentPhoto + 1);
  }

  if (e.key === "ArrowLeft") {
    showPhoto(currentPhoto - 1);
  }

  if (e.key === "Escape") {
    lightbox.classList.remove("is-open");
  }
});